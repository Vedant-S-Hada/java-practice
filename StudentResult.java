// Program 1: Display Student Result using variables

public class StudentResult {

    public static void main(String[] args) {

        // Declaring variables
        String studentName = "Sam";
        int rollNumber = 1;
        double percentage = 99.99;
        char result = 'P';

        // Display output
        System.out.println("Student Result:");
        System.out.println(studentName + " with Roll No " + rollNumber +
                " scored " + percentage + "% and Result is " + result);
    }
}